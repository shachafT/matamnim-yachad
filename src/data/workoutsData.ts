export const WORKOUT_STEPS: WorkoutStep[] = rawSteps.map(row => {
  const [workoutId, order, title, durationOrReps, shortDescription, screenInstruction,
         chairVersion, wallVersion, movementGoal, visualIdea, safetyNote] = row;
  const { isReps, durationSeconds, reps, perSide } = parseDuration(durationOrReps);
  return {
    workoutId,
    order: order as number,
    title,
    durationOrReps,
    shortDescription,
    screenInstruction,
    chairVersion,
    wallVersion,
    movementGoal,
    visualIdea,
    visualType: inferVisualType(visualIdea),
    durationSeconds: isReps ? 0 : durationSeconds,
    isReps,
    reps,
    perSide,
    safetyNote,
  };
});

export function getStepsForWorkout(workoutId: string): WorkoutStep[] {
  return WORKOUT_STEPS.filter(s => s.workoutId === workoutId).sort((a, b) => a.order - b.order);
}
